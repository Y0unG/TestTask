                        <p class="content__title">Епопея зі створенням ВАС</p>
                        <p>
                            Епопея з утворенням антикорупційного суду в Україні триває вже кілька років.
                            Ще у 2016-му Президент Петро Порошенко запевняв українців, що зволікати із судовою реформою
                            не можна і варто діяти. Утім в реальності шлях до створення антикорупційного
                            суду остаточно ще не пройдено і досі.
                        </p>
                        <p>
                            <strong>Улітку 2016-го Рада ухвалила внесений Президентом законопроект "Про судоустрій  і статус суддів"</strong>,
                            який відкладав створення антикорупційного суду на невизначений термін. У липні після підписання тексту
                            закону в Україні з'явився нова судова інституція. Утім лише на папері.
                        </p>
                        <p>
                            <strong>У 2017 році</strong> депутати подали до парламенту два законопроекти щодо ВАС: <strong>№ 6011</strong>
                            передбачав створення
                            Вищого антикорупційного суду та незалежну апеляційну палату, а № 6529 – створення антикорупційних
                            палат у чинних судових установах. У жовтні 2017 року Венеціанська комісія – дорадчий орган Ради
                            Європи, який дає висновки щодо відповідності проекту законодавства європейським стандартам та
                            цінностям – надала обом документам оцінку. Венеційка рекомендувала Президенту подати свій
                            законопроект про створення антикорупційного суду з чіткими рекомендаціями стосовно процедури
                            відбору суддів та функціонування майбутнього судового органу.
                        </p>
                        <p>
                            <strong>7 червня 2018 року Верховна рада таки ухвалила Закон України "Про Вищий антикорупційний суд" (ВАС).</strong>
                            2 серпня Президент України Петро Порошенко підписав Закон "Про внесення змін до Закону
                            України" Про судоустрій та статус суддів "у зв'язку із прийняттям Закону України" Про Вищий
                            антикорупційний суд". Таким чином були усунені всі законодавчі перепони для
                            створення та ефективної роботи ВАС.
                        </p>
