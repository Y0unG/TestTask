                        <p class="content__title">Відбір суддів до ВАС</p>
                        <p>
                            Вищий антикорупційний суд дійсно може стати ефективною і неупередженою судовою
                            інституцією. Утім це багато в чому залежить від якості суддів новоствореного ВАС. І на цьому етапі
                            дуже важлива роль відведена громадянському суспільству та міжнародному співтовариству. Саме на них
                            лягає відповідальність контролювати кожен крок у процедурі відбору, щоб уникнути
                            будь-яких можливих маніпуляцій.
                        </p>
                        <p>
                            2 серпня Вища кваліфікаційна комісія суддів України (ВККС) оголосила <strong>конкурс на зайняття
                            39 вакантних посад суддів</strong> Вищого антикорупційного суду: 27 посад – безпосередньо
                            у Вищому антикорупційному суді та 12 посад – в Апеляційній палаті ВАС. Прийом
                            документів тривав з 8 серпня по 14 вересня.
                        </p>
                        <p>
                            Закон України «Про Вищий антикорупційний суд» говорить, що на посаду судді Вищого
                            антикорупційного суду може бути призначений громадянин України, якщо він, зокрема,
                            відповідає одній із таких вимог:
                        </p>
                        <ol>
                            <li>має стаж роботи на посаді судді не менше п’яти років;</li>
                            <li>має науковий ступінь у сфері права та стаж наукової роботи у сфері права щонайменше сім років;</li>
                            <li>має досвід професійної діяльності адвоката, у тому числі щодо здійснення представництва в
                                суді та/або захисту від кримінального обвинувачення щонайменше сім років;</li>
                            <li>має сукупний стаж (досвід) зазначеної у пунктах 1–3 цієї частини роботи
                                (професійної діяльності) щонайменше сім років.</li>
                        </ol>
                        <p>Зрештою, до ВАС подали документи 342 кандидати. З них – 45% судді, 34% адвокати, 14% -
                            науковці. рештамає – сукупний стаж. До іспитів було допущено 270 кандидатів. Після анонімного
                            тестування залишилося 156 осіб. Станом на кінець 2018 року кандидати склали письмові іспити.</p>
