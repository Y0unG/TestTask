                        <p class="content__title">Роль громадськості у запуску ВАС</p>
                        <p><strong>Transparency International Ukraine</strong> взяла на себе обов’язок підтримати
                            формування та запуск Вищого антикорупційного суду. Ми послідовно виступали
                            за формування окремого Антикорупційного суду, який би брав до розгляду справи,
                            підслідні НАБУ та САП. Наша організація неухильно відстоювала “суттєву роль” Громадської
                            ради міжнародних експертів у процесі добору суддів майбутнього суду.
                            Спільно з іншими організаціями громадянського суспільства та міжнародними партнерами
                            України нам вдалося добитися ухвалення Закону України “Про Вищий антикорупційний суд”
                            у редакції, що відповідає інтересам українського суспільства.</p>
                        <img class="content__img" src="./img/ti-society.jpg" alt="society">
                        <p>Так, ми брали активну участь в адвокаційній кампанії, аби переконати депутатів
                            від усіх парламентських фракцій ухвалити законопроекти для створення ВАС.
                            Окремо ТІ брала участь у засіданнях парламентських комітетів та вносила свої рекомендації
                            та просувала правки до чинних редакцій законів.
                        </p>
                        <p>
                            Також ТІ Україна провела окрему адвокаційну кампанію із залучення кандидатів до
                            конкурсу у ВАС. ТІ Україна постійно підтримує зв'язок з усіма зацікавленими сторонами
                            процесу, включаючи політиків, неурядові організації та правову спільноту.
                        </p>
                        <p>
                            Нині ТІ Україна зосереджена на контролі процесу відбору суддів ВАС. Ми наполягаємо
                            на тому, що Вища кваліфікаційна комісія суддів України має негайно забезпечити
                            належні умови роботи для міжнародних експертів та ухвалити правила, які
                            гарантуватимуть прозорість конкурсу до антикорупційного суду. Ідеться насамперед про
                            повноцінний доступ членів ГРМЕ та її секретаріату до матеріалів щодо кандидатів та
                            практичних завдань, виконаних кандидатами в рамках іспиту.
                        </p>
                        <p>
                            9 січня ТІ Україна разом з партнерами з Центру протидії корупції, Фундації DEJURE та
                            Автомайдану презентували <a
                                href="https://ti-ukraine.org/news/povnyj-spysok-negidnyh-kandydativ-do-vas/">список негідних</a>
                            Антикорупційного суду. Ми проаналізували майно, бізнесові та політичні зв’язки кандидатів та
                            їхніх родин, а також попередні судові рішення кандидатів. Загалом у списку претендентів,
                            репутація яких під сумнівом, <a
                                href="https://ti-ukraine.org/news/polovyna-kandydativ-v-antykorsud-ye-sumnivnymy-vysnovok-gromadskosti/">55 із 113</a>
                            претендентів, які раніше успішно склали іспити до Вищого антикорупційного суду.<br><br>
                            З 55 сумнівних кандидатів — 42 суддів та екс-суддів, 13 адвокатів та науковців. Найбільше
                            питань у громадськості викликали можливі недостовірне декларування та приховування статків
                            через оформлення на родичів.<br><br>
                            Усі ці матеріали були надані як Громадській раді міжнародних експертів, так і Вищій
                            кваліфікаційній комісії суддів. Примітно, що 38 кандидатів з цього переліку потрапили до
                            “чорного списку” ГРМЕ і були винесені на спільне засідання з ВККС.
                        </p>
                        <p>
                            <strong>Зрештою, наша спільна мета – створити незалежний, неупереджений та ефективний
                            антикорупційний суд.</strong> Новостворена судова установа може посилити наступ на
                            корупцію в Україні та стати прикладом для інших країн. Народження антикорупційної судової
                            системи не було легким, але це може призвести до дивовижних наслідків. <br><br>
                            Історія антикорупційного суду тільки розпочинається, і найцікавіша частина все ще попереду.
                            Тримаємо фокус.
                        </p>
