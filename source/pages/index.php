                        <p class="content__title">Роль Громадської ради міжнародних експертів</p>
                        <p>Участь міжнародних експертів у доборі суддів наполегливо відстоювали в процесі
                            ухвалення закону про антикорупційний суд, і є унікальним міжнародним прецедентом.
                            Адже аналогів Громадській раді міжнародних експертів з такими повноваженнями
                            у світі фактично немає.
                        </p>
                        <p>
                            <strong>ГРМЕ</strong> збирає, перевіряє та аналізує інформацію щодо кандидатів на посади
                            суддів ВАС, надає ВККС інформацію щодо кандидатів, може ініціювати проведення спеціального
                            спільного засідання з ВККС щодо встановлення відповідності кандидатів критеріям
                            доброчесності та компетентності.
                        </p>
                        <p>
                            Таке спільне засідання повинно бути проведено протягом 30 днів з моменту
                            оголошення результатів іспиту. Якщо більшість членів ГРМЕ не проголосують за
                            відповідність кандидата таким критеріям, він припиняє участь в конкурсі.
                        </p>
                        <p>
                            <strong>ГРМЕ складається з 6 членів з бездоганною діловою репутацією та значним
                                професійним досвідом, 4 – на посаді судді, 2 – на посаді прокурора.</strong>
                        </p>
                        <p>
                            Члени ГРМЕ прибули в Україну восени 2018 року і провели перші зустрічі із ВККС та профільними
                            громадськими організаціями з метою узгодження спільної діяльності. Попри це, ГРМЕ
                            потрібно ще затвердити регламент власної роботи, критерії оцінки кандидатів, а також
                            правила проведення спеціальних спільних засідань із ВККС.
                            <br>Міжнародні експерти вступають у процес добору одразу після оголошення
                            результатів іспиту.
                        </p>
                        <p>
                            26 грудня ВККС оприлюднило результати письмового завдання, тим самим давши зелене світло
                            міжнародним експертам. Саме з того дня стартувало 30 днів для ГРМЕ, аби заблокувати участь
                            недоброчесних кандидатів. <br>
                            Спершу Громадська рада міжнародних експертів затвердила власний регламент роботи та
                            <a href="https://pcie.org.ua/storage/files/pcie-rules-of-procedure-ukr-eng.pdf">критерії оцінки</a>.
                            З 4 по 15 січня ГРМЕ провела 5 зустрічей, за наслідками яких винесла на спільне спеціальне
                            засідання з ВККС
                            <a href="https://ti-ukraine.org/news/49-z-113-kandydativ-do-vas-sumnivni-dumka-mizhnarodnykiv/">49 кандидатів</a>.
                            З них — 36 суддів та екс-суддів, 9 адвокатів і 4 науковця.<br>
                            18 січня відбулося перше спільне засідання ВККС і ГРМЕ. На ньому визнали такими, що не
                            відповідають критеріям доброчесності, перших
                            <a href="https://ti-ukraine.org/news/8-kandydativ-pokynuly-konkurs-do-antykorsudu/">8 кандидатів</a>.
                            Загалом ГРМЕ разом з ВККС провело 6 зустрічей та заблокувало
                            <a href="https://ti-ukraine.org/news/42-kandydata-pokynuly-konkurs-do-antykorsudu/">42 претендента</a>
                            на посади суддів Вищого антикорупційного суду. З них — 31 суддя та екс-суддя, 5 адвокатів і
                            6 науковців.<br>
                            30 січня ВККС розпочала етап співбесід з 71 кандидатом за результатами дослідження досьє кандидатів.
                        </p>
